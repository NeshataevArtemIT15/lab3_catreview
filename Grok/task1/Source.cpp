std::string FTelegraph::EncodeMessage(const std::wstring& Input)
{
    std::string Result;
    bool bFirstLetterInWord = true;  // true => следующая буква начинает новое слово

    for (wchar_t C : Input)
    {
        if (C == L' ' || C == L'\t' || C == L'\n' || C == L'\r')
        {
            bFirstLetterInWord = true;
            continue;
        }

        auto It = MorseDictionary.find(C);

        if (It != MorseDictionary.end())
        {
            if (!Result.empty())
            {
                // Между словами — двойной пробел, между буквами — одинарный
                Result += bFirstLetterInWord ? "  " : " ";
            }

            Result += It->second;
            bFirstLetterInWord = false;
        }
        // Неподдерживаемые символы игнорируются
    }

    return Result;
}